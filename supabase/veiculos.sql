-- AutoGest Pro · tabela de VEÍCULOS (frota) + carga inicial
-- ─────────────────────────────────────────────────────────────
-- Rode este script no SQL Editor do Supabase para criar a tabela
-- `veiculos` e carregar a frota inicial (placa → proprietário).
--
-- A aba "Premissas" do app usa esta tabela para:
--   • listar todos os carros
--   • incluir novos veículos
--   • editar a placa e ALTERAR o proprietário
--   • excluir veículos
--
-- Observação: o app também cria/popula a tabela automaticamente na
-- primeira vez (quando ela existe e está vazia). Este script é a forma
-- manual/explícita de fazer o mesmo.
-- ─────────────────────────────────────────────────────────────

create table if not exists public.veiculos (
  id          bigint generated always as identity primary key,
  placa       text not null,
  dono        text,
  modelo      text,
  created_at  timestamptz default now(),
  unique (placa)
);

create index if not exists idx_veiculos_dono on public.veiculos (dono);

alter table public.veiculos enable row level security;

drop policy if exists "anon full access veiculos" on public.veiculos;
create policy "anon full access veiculos"
  on public.veiculos
  for all
  to anon
  using (true)
  with check (true);

